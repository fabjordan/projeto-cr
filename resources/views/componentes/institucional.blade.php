		<div class="col-md-9 col-xs-12">	<p>

			CARTÓRIOS MARANHÃO é a marca da CENTRAL ÚNICA DE SERVIÇOS ELETRÔNICOS COMPARTILHADOS, criada pelo Provimento nº 13/2016 da Corregedoria Geral da Justiça do Maranhão, e mantido pela Associação dos Titulares de Cartórios do Maranhão.<br><br>

			Tem a função de centralizar as informações das Serventias Extrajudiciais do Maranhão, conforme determina o Conselho Nacional de Justiça nos Provimentos nº 46, 47 e 48.<br><br>

			Constitui ferramenta destinada a facilitar e modernizar o acesso dos usuários às serventias extrajudiciais maranhenses, mediante a possibilidade de busca de informações, solicitação de certidões, envio e apresentação de documentos (escrituras, instrumentos particulares, e outros documentos gerados eletronicamente).<br><br>

			A Cartórios Maranhão é responsável por operacionalizar o Sistema de Registro Eletrônico de Imóveis no Estado do Maranhão, previsto pela Lei nº 11.977/2009, que impõe a integração das Serventias de Registro de Imóveis ao universo tecnológico do registro eletrônico. Assim, os usuários poderão apresentar títulos e documentos para registro mediante utilização da Cartórios Maranhão, com a comodidade das ferramentas digitais disponíveis, sem ter que se deslocar fisicamente ao cartório.<br>

			Acreditamos que a Cartórios Maranhão permitirá aos usuários vivenciarem uma experiência totalmente nova, eficiente e segura. Um dos objetivos da unificação das informações registrais e notariais é de revitalizar a imagem dos Cartórios perante a sociedade, buscando acompanhar o que há de mais avançado em tecnologia web, aliando segurança jurídica, segurança digital e comodidade.<br>

